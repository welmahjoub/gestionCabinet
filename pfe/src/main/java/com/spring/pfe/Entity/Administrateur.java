package com.spring.pfe.Entity;

public class Administrateur  extends User{

	  /*========================================================================================================================*/
	  /*========================================= Attrubut  ================================================*/
	  /*========================================================================================================================*/
		
	
	
	  /*========================================================================================================================*/
	  /*========================================= Methods  ================================================*/
	  /*========================================================================================================================*/
		

	private static final long serialVersionUID = 1L;

	public Administrateur() {
		super();
	
	}

	public Administrateur(String cin, String nomprenom, String login, String password, String image) {
		
		super(cin, nomprenom, login, password, image);
		
	}

	@Override
	public String toString() {
		return "Administrateur [cin=" + cin + ", nomprenom=" + nomprenom + ", login=" + login + ", password=" + password
				+ ", image=" + image + "]";
	}

	  /*========================================================================================================================*/
	  /*=========================================   ================================================*/
	  /*========================================================================================================================*/
		
}
